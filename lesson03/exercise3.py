x = int(input("Give x: "))
y = int(input("Give y: "))

if x == y:
    print(str(x) + "==" + str(y))

if x != y:
    print(str(x) + "!=" + str(y))

if x < y:
    print(str(x) + "<" + str(y))

if x <= y:
    print(str(x) + "<=" + str(y))

if x > y:
    print(str(x) + ">" + str(y))

if x >= y:
    print(str(x) + ">=" + str(y))

